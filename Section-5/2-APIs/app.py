# APIs
# what is an API?
# API stands for Application Programming Interface. It is a set of rules and protocols that allow one software application to interact with another. APIs are used to define how different software components should communicate with each other. They are commonly used to enable integration between different systems or services.

# for example Yelp API, Twitter API, Google Maps API, etc.
# Yelp API allows developers to access business data and reviews from Yelp's database.
# https://www.yelp.com/developers/documentation/v3/business_search

# API endpoints are URLs that define the entry points for interacting with the API. They specify the resources that can be accessed and the operations that can be performed on those resources.
# GET /api/v1/products # Retrieve a list of products
# POST /api/v1/products  # Create a new product
# PUT /api/v1/products/123 # Update product with ID 123
# DELETE /api/v1/products/123 # Delete product with ID 123